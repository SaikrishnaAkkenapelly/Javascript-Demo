let target = 100;
let battersLeft = 4;

//if statement
if(battersLeft > 5 && target < 100)
{
    console.log("Possible");
}

//if else statement
if(battersLeft > 5 && target < 100)
{
    console.log("Possible");
}
else
{
    console.log("Not Possible");
}

//else if statement
if(battersLeft > 5 && target < 100)
{
    console.log("Possible");
}
else if(battersLeft > 3 && target < 100)
{
    console.log("Still Possible...");
}
else
{
    console.log("Not Possible");
}